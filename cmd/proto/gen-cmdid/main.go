func getCmdID(msg *protogen.Message) int32 {
	opts := msg.Desc.Options()
	if !proto.HasExtension(opts, pb.E_CmdId) {
		return 0
	}
	val := proto.GetExtension(opts, pb.E_CmdId)
	if v, ok := val.(int32); ok {
		return v
	}
	return 0
}
