package main

import (
	"cake/proto/pb"
	"fmt"
	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/proto"
	"strings"
)

const (
	globalFileName = "cmdid_global_map.pb.go"
	headerComment  = "// Code generated by gen-proto-routers. DO NOT EDIT."
	globalPkg      = "pb"
)

func main() {
	protogen.Options{}.Run(func(plugin *protogen.Plugin) error {
		var allMsgs []*protogen.Message
		for _, file := range plugin.Files {
			if !file.Generate {
				continue
			}
			allMsgs = append(allMsgs, file.Messages...)
		}

		if len(allMsgs) == 0 {
			return nil
		}

		g := plugin.NewGeneratedFile(globalFileName, globalPkg)

		g.P(headerComment)
		g.P()
		g.P("package ", globalPkg)
		g.P()

		g.P("import (")
		g.P(`	"google.golang.org/protobuf/proto"`)
		g.P(`	"reflect"`)
		g.P(")")
		g.P()

		genAllMap(g, allMsgs)
		genHelperFunc(g)

		return nil
	})
}

